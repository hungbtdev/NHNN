using KTApps.Domain;
using System.ComponentModel.DataAnnotations;

namespace KTApps.Core.NotificationService.Entities
{
    public class CoreContentReplaces : KTAppDomain
    {
        [Required]
        [MaxLength(255)]
        public string OldChars { set; get; }
        [Required]
        [MaxLength(255)]
        public string NewChars { set; get; }
        [Required]
        [MaxLength(1000)]
        public string Description { set; get; }
    }
}
